			containerLoad: $('.containerLoad')
		}
		$('input.btn-submit-medida').on('click', function(){
			$.ajax({
				url: 'ajax/delay.php',
				beforeSend: function(){
					res.containerLoad.append(res.loadingajax);
				},
				success: function(data){
					res.containerLoad.html(data);
					res.containerLoad.find(res.loadingajax).remove();
